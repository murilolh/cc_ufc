void reshape(int width, int height){
  WIDTH=width;
  HEIGHT=height;
  glViewport(0,0,(GLint)width,(GLint)height);
  glMatrixMode(GL_PROJECTION);
  glLoadIdentity();
  gluPerspective(400.0,width/(float)height,0.1,400.0);
  glMatrixMode(GL_MODELVIEW);
}

